/**
 * add - add two numbers
 * @a: num
 * @b: num
 *
 * Return: int
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtract two numbers
 * @a: num
 * @b: num
 *
 * Return: int
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiply two numbers
 * @a: num
 * @b: num
 *
 * Return: int
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divide two numbers
 * @a: num
 * @b: num
 *
 * Return: int
 */
float div(int a, int b)
{
	return (a / b);
}

/**
 * mod - find modulo of two numbers
 * @a: num
 * @b: num
 *
 * Return: int
 */
int mod(int a, int b)
{
	return (a % b);
}
